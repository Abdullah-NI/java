package _02_conditionals;

public class _07_RelationalOprater {
    public static void main(String[] args) {

        int a=5;
        int b=7;
        System.out.println(a==b);
        System.out.println(a!=b);
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a>=b);
        System.out.println(a<=b);
    }
}
