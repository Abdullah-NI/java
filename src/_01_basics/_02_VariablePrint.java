package _01_basics;

public class _02_VariablePrint {
    public static void main(String[] args) {
        int x;
        x=6;
        int y=4,z=8;
        System.out.println(x);
        System.out.println(x+y+z);

        System.out.println();

        x=x+7;
        System.out.println(x);
        x=x-20;
        System.out.println(x);
        x+=1;
        System.out.println(x);
        x*=-1;
        System.out.println(x);
        x/=2;
        System.out.println(x);

    }
}
