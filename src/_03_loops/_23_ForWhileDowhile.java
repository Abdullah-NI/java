package _03_loops;

public class _23_ForWhileDowhile {
    public static void main(String[] args) {

        for(int i=11;i<10;i++){
            System.out.println(i);
        }

        int i=11;
        while(i<10){
            System.out.println(i);
            i++;
        }

        int j=11;
        do{
            System.out.println(j);
            j++;
        }while(i<10);
    }
}
