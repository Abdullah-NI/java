package _03_loops;

import java.util.Scanner;

public class _17_BasicOfLoops {
    public static void main(String[] args) {
        Scanner sc =new Scanner(System.in);
        int n=sc.nextInt();
        for(int i=1;i<=n;i++){
            System.out.println("Hellow world");
        }
        for(int i=1;i<100;i++){
            System.out.print(i+" ");
        }
    }
}
